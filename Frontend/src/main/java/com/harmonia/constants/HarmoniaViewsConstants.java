package com.harmonia.constants;

/**
 * The HarmoniaViewsConstants class contains constants for the FXML view files used in the Harmonia application.
 * Each constant represents the file path of the corresponding FXML view file.
 * These constants are used to load the appropriate view files in the application.
 * This class should not be instantiated.
 *
 * @author Harmonia Team
 * @version 1.0
 */
public class HarmoniaViewsConstants {

    /**
     * The PROFILE_VIEW constant represents the name of the FXML view for the user's profile page.
     */

    public static final String PROFILE_VIEW = "profile-view.fxml";

    /**
     * The USER_SETTINGS_VIEW constant represents the name of the FXML view for the user's settings page.
     */
    public static final String USER_SETTINGS_VIEW = "usersettings-view.fxml";

    /**
     * The USER_SERVERS_VIEW constant represents the name of the FXML view for the user's server/community list.
     */
    public static final String USER_SERVERS_VIEW = "mycommunities-view.fxml";

    /**
     * The HOME_VIEW constant represents the name of the FXML view for the main application page.
     */
    public static final String HOME_VIEW = "harmonia-view.fxml";

    /**
     * The LOGIN_VIEW constant represents the name of the FXML view for the login page.
     */
    public static final String LOGIN_VIEW = "login-view.fxml";

    /**
     * The DIRECT_MESSAGES_VIEW constant represents the name of the FXML view for direct messaging.
     */
    public static final String DIRECT_MESSAGES_VIEW = "messages-view.fxml";
}
